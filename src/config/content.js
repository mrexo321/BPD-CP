export function getStoredContent() {
    try {
        const stored = localStorage.getItem("bpd-content");
        if (stored) return { ...defaultContent, ...JSON.parse(stored) };
    } catch { /* ignore */ }
    return { ...defaultContent };
}

export function saveContent(content) {
    localStorage.setItem("bpd-content", JSON.stringify(content));
}
